def luasSegitiga(a,t):
    luas = a * t / 2
    return luas

alasSegitiga1 = 10
alasSegitiga2 = 15
tinggiSegitiga1 = 20
tinggiSegitiga2 = 45

print('luas segitiga dengan alas',alasSegitiga1,
      'dan tinggi',tinggiSegitiga1,'adalah',
      luasSegitiga(alasSegitiga1,tinggiSegitiga1))

print('luas segitiga dengan alas',alasSegitiga2,
      'dan tinggi',tinggiSegitiga2,'adalah',
      luasSegitiga(alasSegitiga2,tinggiSegitiga2))
